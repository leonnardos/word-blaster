/// Banco de palavras (~1000) em 23 tópicos.
///
/// O jogo sorteia palavras ALEATORIAMENTE entre todos os tópicos; o nível
/// controla a dificuldade (tamanho da palavra), não o tema.
///
/// PARA ADICIONAR PALAVRAS: inclua uma linha
///   Word('palavra em inglês', 'tradução em português'),
/// dentro do tópico desejado. Regras:
///  - inglês sempre em minúsculas;
///  - sem repetir palavra dentro do mesmo tópico;
///  - frases com espaço são permitidas (aparecem só em níveis altos).
class Word {
  final String en;
  final String pt;

  const Word(this.en, this.pt);
}

class WordCategory {
  final int level;
  final String name;
  final List<Word> words;

  const WordCategory(this.level, this.name, this.words);
}

/// Palavras candidatas ao sorteio para um [level] e um conjunto de tópicos
/// escolhidos pelo jogador ([topics] vazio = todos os tópicos).
///
/// O nível controla a dificuldade: palavras de até `4 + level` letras e
/// frases só a partir do nível 6 (nível 12+ libera tudo). Se esses filtros
/// deixarem o pool pequeno demais (ex.: só "Conversação" escolhido, que é
/// quase só frases), eles são relaxados — escolher um tópico nunca pode
/// deixar o jogo sem variedade, muito menos sem palavras.
/// Nome do tópico em que a palavra aparece (primeira ocorrência).
String? topicOfWord(String en) {
  for (final category in wordBank) {
    for (final w in category.words) {
      if (w.en == en) return category.name;
    }
  }
  return null;
}

List<Word> candidateWords({
  required int level,
  Set<String> topics = const {},
  Set<String> excludeWords = const {},
  Set<String> excludeInitials = const {},
}) {
  final categories = topics.isEmpty
      ? wordBank
      : wordBank.where((c) => topics.contains(c.name)).toList();

  List<Word> collect({required bool applyDifficulty}) {
    final maxLen = 4 + level;
    final pool = <Word>[];
    for (final category in categories) {
      for (final w in category.words) {
        if (applyDifficulty) {
          final isPhrase = w.en.contains(' ');
          if (isPhrase && level < 6) continue;
          if (!isPhrase && level < 12 && w.en.length > maxLen) continue;
        }
        if (excludeWords.contains(w.en)) continue;
        if (excludeInitials.contains(w.en[0])) continue;
        pool.add(w);
      }
    }
    return pool;
  }

  // Menos de 8 candidatos vira loop da mesma palavra (com 1 candidato o
  // sorteio ponderado nem existe); nesse caso o filtro de dificuldade cai.
  const minPool = 8;
  final gated = collect(applyDifficulty: true);
  if (gated.length >= minPool) return gated;
  final relaxed = collect(applyDifficulty: false);
  return relaxed.isEmpty ? gated : relaxed;
}

